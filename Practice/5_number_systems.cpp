#include <iostream>

int main() {
    int binary_num = 0b10001;
    int octal_num = 021;
    int decimal_num = 17;
    int hexadecimal_num = 0x11;

    std::cout << "binary: " << binary_num << std::endl;
    std::cout << "octal: " << octal_num << std::endl;
    std::cout << "decimal: " << decimal_num << std::endl;
    std::cout << "hexadecimal: " << hexadecimal_num << std::endl;
}
